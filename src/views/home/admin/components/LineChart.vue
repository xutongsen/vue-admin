<template>
  <div :id="id" :class="className" :style="{height:height,width:width}" />
</template>

<script>
import echarts from 'echarts'
require('echarts/theme/macarons') // echarts theme
import resize from './mixins/resize'

import { defineComponent, mixins, watch, props,onMounted, onUnmounted, ref, computed, reactive } from '@vue/composition-api';

export default defineComponent({
  props: {
    id: {
      type: String,
      default: 'chart'
    },
    className: {
      type: String,
      default: 'chart'
    },
    width: {
      type: String,
      default: '100%'
    },
    height: {
      type: String,
      default: '350px'
    },
    autoResize: {
      type: Boolean,
      default: true
    },
    chartData: {
      type: Object,
      required: true
    }
  },
  setup(props, { root, data }) {
    let chartObj = reactive({
      chart: null
    })

    const initChart = () => {
      chartObj.chart = echarts.init(document.getElementById(props.id), 'macarons')
      setOptions(props.chartData)
    }
    

    const setOptions = ({ expectedData, actualData } = {}) => {
      chartObj.chart.setOption({
        xAxis: {
          data: ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'],
          boundaryGap: false,
          axisTick: {
            show: false
          }
        },
        grid: {
          left: 10,
          right: 10,
          bottom: 20,
          top: 30,
          containLabel: true
        },
        tooltip: {
          trigger: 'axis',
          axisPointer: {
            type: 'cross'
          },
          padding: [5, 10]
        },
        yAxis: {
          axisTick: {
            show: false
          }
        },
        legend: {
          data: ['expected', 'actual']
        },
        series: [{
          name: 'expected', itemStyle: {
            normal: {
              color: '#FF005A',
              lineStyle: {
                color: '#FF005A',
                width: 2
              }
            }
          },
          smooth: true,
          type: 'line',
          data: expectedData,
          animationDuration: 2800,
          animationEasing: 'cubicInOut'
        },
        {
          name: 'actual',
          smooth: true,
          type: 'line',
          itemStyle: {
            normal: {
              color: '#3888fa',
              lineStyle: {
                color: '#3888fa',
                width: 2
              },
              areaStyle: {
                color: '#f3f8ff'
              }
            }
          },
          data: actualData,
          animationDuration: 2800,
          animationEasing: 'quadraticOut'
        }]
      })
    }
    onMounted(() => {
      root.$nextTick(() => {
        initChart()
      })
    })
    resize(root,chartObj)
   

    onUnmounted(() => {
      if (!chartObj.chart) {
        return
      }
      chartObj.chart.dispose()
      chartObj.chart = null
    })

    watch(() => props.chartData,(chartData)=>{
      setOptions(chartData)
    },{
      lazy: true,
      deep: true
    }) 
  
  },

})
</script>
